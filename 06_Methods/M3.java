class M3
{
	public static void main(String[] args) 
	{
			addition(10, 20);

			subraction(42, 4);

			addition(10, 30/2);
	}

	public static void addition(int a, int b)
	{
		int res = a+b;
		System.out.println(res);
	}

	public static void subraction(int a, int b)
	{
		int res = a-b;
		System.out.println(res);
	}
}