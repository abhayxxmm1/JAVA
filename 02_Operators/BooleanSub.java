class BooleanSub
{
	public static void main (String[ ] args)
	{
		boolean a = true;
		boolean b = true;


		boolean sub = a - b; 
		boolean add = a + b;
		boolean mul = a * b;  
		boolean div = a / b;  
		boolean mod = a % b;  

		System.out.println(sub);
		System.out.println(add);
		System.out.println(mul);
		System.out.println(div);
		System.out.println(mod);
	}
}